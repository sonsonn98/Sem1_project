                    <h2 class="story__title">OUR SELECTION PROCESS</h2>
                    <p class="story__text">Each year, we reach out to thousands of the world’s most experienced travel
                        professionals, and ask them to vote for what they think is the best beach on earth. Voters are
                        not given any specific criteria as to what makes a beach the best, but they are asked to
                        describe why they think it is the best. Each vote was accompanied by a compelling description,
                        adding depth to the selection process and making sure extra thought was put into the vote.</p>
                    <p class="story__text">As many of the world’s best beaches are yet to be discovered, our final list
                        is not based purely on votes cast. The number of votes a beach receives is a contributing factor
                        to its appearance on our list as well as its ranking. In order to be included in our annual
                        list, a beach must receive at least one vote.</p>
                    <p class="story__text">Once all votes are cast, our team reviews each submission thoroughly and
                        debates the merits of each beach that received a vote. Additionally, we collaborate with our
                        esteemed Beach Ambassadors, a carefully chosen group of individuals renowned for their extensive
                        travel experiences particularly to the world's top beaches. Each Ambassador has the privilege of
                        nominating three beaches, further enriching our pool of candidates.</p>
                    <p class="story__text">We then compile our final list based on our specific criteria:</p>
